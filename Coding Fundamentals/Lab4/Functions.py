#1 Find the max of three numbers
def maxofthree(number):
    maxnumber = max(number)
    return(maxnumber)
    
number = (10, 20, 30)
maxnumber = maxofthree(number)
print(maxnumber)

#2 Write a Python function to sum all the numbers in a list
def allsum(numbers):
    answers = sum(numbers)
    return(answers)

numbers = (8, 2, 3, 0, 7)
answers = allsum(numbers)
print(answers)

